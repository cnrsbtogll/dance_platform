import React, { useState, useEffect, useCallback } from 'react';
import { useForm } from 'react-hook-form';
import { doc, updateDoc, getDoc, setDoc, writeBatch, collection, getDocs, query, orderBy } from 'firebase/firestore';
import { db, auth, storage } from '../../../api/firebase/firebase';
import { ref, uploadString, getDownloadURL } from 'firebase/storage';
import { User, DanceLevel, DanceStyle as DanceStyleType } from '../../../types';
import ImageUploader from '../../../common/components/ui/ImageUploader';
import { toast } from 'react-hot-toast';
import CustomSelect from '../../../common/components/ui/CustomSelect';
import CustomPhoneInput from '../../../common/components/ui/CustomPhoneInput';
import { useNavigate } from 'react-router-dom';
import { updateProfile } from 'firebase/auth';
import CustomInput from '../../../common/components/ui/CustomInput';

interface InstructorProfileFormProps {
  user: User;
}

// Dans stilleri i√ßin tip tanƒ±mƒ±
interface DanceStyleOption {
  id: string;
  label: string;
  value: string;
}

// Form verisi i√ßin tip tanƒ±mƒ±
interface InstructorProfileFormData {
  displayName: string;
  bio: string;
  specialties: string[];
  experience: string;
  phoneNumber: string;
  countryCode: string;
  location: string;
  photoURL: string;
  // User table fields
  gender: string;
  age: number | undefined;
  level: DanceLevel;
  city: string;
  height?: number;
  weight?: number;
  danceStyles: string[];
  availableTimes: string[];
  // Additional fields
  createdAt?: string;
  updatedAt?: string;
  isActive?: boolean;
  role?: string;
  userId?: string;
}

interface DanceStyle {
  id: string;
  label: string;
  value: string;
}

const InstructorProfileForm: React.FC<InstructorProfileFormProps> = ({ user }) => {
  const navigate = useNavigate();
  const [activeTab, setActiveTab] = useState<'user' | 'instructor'>('user');
  const [danceStyles, setDanceStyles] = useState<DanceStyleOption[]>([]);
  const [loadingStyles, setLoadingStyles] = useState(true);
  console.log('üîµ Component rendered with user:', user);

  const [loading, setLoading] = useState(false);
  const [initialLoading, setInitialLoading] = useState(true);
  const [saveSuccess, setSaveSuccess] = useState(false);
  const [profilePhotoURL, setProfilePhotoURL] = useState<string>(user.photoURL || '');
  const [selectedSpecialties, setSelectedSpecialties] = useState<string[]>([]);
  const [fetchError, setFetchError] = useState<string | null>(null);
  const [resetImageUploader, setResetImageUploader] = useState(false);
  const { register, handleSubmit, setValue, watch, reset, getValues } = useForm<InstructorProfileFormData>({
    defaultValues: {
      displayName: '',
      bio: '',
      specialties: [],
      experience: '',
      phoneNumber: '',
      countryCode: '+90',
      location: '',
      photoURL: '',
      gender: '',
      age: undefined,
      level: 'beginner',
      city: '',
      height: undefined,
      weight: undefined,
      danceStyles: [],
      availableTimes: []
    }
  });

  // Kullanƒ±cƒ± kontrol√º
  useEffect(() => {
    if (!user?.id) {
      console.error('‚ùå Invalid user state:', user);
      toast.error('Ge√ßersiz kullanƒ±cƒ± bilgisi');
      navigate('/'); // Ana sayfaya y√∂nlendir
      return;
    }
  }, [user, navigate]);

  // Dans stilleri bilgilerini getiren fonksiyon
  const fetchDanceStyles = useCallback(async () => {
    if (danceStyles.length > 0) {
      console.log("Dans stilleri zaten y√ºklenmi≈ü, tekrar y√ºklenmiyor");
      return;
    }

    setLoadingStyles(true);
    try {
      const danceStylesRef = collection(db, 'danceStyles');
      const q = query(danceStylesRef, orderBy('label'));
      const querySnapshot = await getDocs(q);
      
      const styles: DanceStyleOption[] = [];
      
      querySnapshot.forEach((doc) => {
        const styleData = doc.data() as Omit<DanceStyleOption, 'id'>;
        styles.push({
          id: doc.id,
          label: styleData.label,
          value: styleData.value
        });
      });

      if (styles.length === 0) {
        // Firestore'da stil yoksa varsayƒ±lan stiller
        const defaultStyles = [
          { id: 'salsa', label: 'Salsa', value: 'salsa' },
          { id: 'bachata', label: 'Bachata', value: 'bachata' },
          { id: 'kizomba', label: 'Kizomba', value: 'kizomba' },
          { id: 'tango', label: 'Tango', value: 'tango' },
          { id: 'vals', label: 'Vals', value: 'vals' }
        ];
        setDanceStyles(defaultStyles);
      } else {
        setDanceStyles(styles);
      }
    } catch (error) {
      console.error('Dans stilleri y√ºklenirken hata:', error);
      // Hata durumunda varsayƒ±lan stiller
      const defaultStyles = [
        { id: 'salsa', label: 'Salsa', value: 'salsa' },
        { id: 'bachata', label: 'Bachata', value: 'bachata' },
        { id: 'kizomba', label: 'Kizomba', value: 'kizomba' },
        { id: 'tango', label: 'Tango', value: 'tango' },
        { id: 'vals', label: 'Vals', value: 'vals' }
      ];
      setDanceStyles(defaultStyles);
    } finally {
      setLoadingStyles(false);
    }
  }, [danceStyles.length]);

  // Dans stillerini component mount olduƒüunda y√ºkle
  useEffect(() => {
    fetchDanceStyles();
  }, [fetchDanceStyles]);

  // Form deƒüerlerini izle ve logla
  useEffect(() => {
    const subscription = watch((value, { name, type }) => {
      console.log('üîÑ Form field changed:', { field: name, value, type });
    });
    return () => subscription.unsubscribe();
  }, [watch]);

  useEffect(() => {
    const fetchInstructorProfile = async () => {
      if (!user?.id) {
        console.error('‚ùå User ID not found');
        setInitialLoading(false);
        setFetchError('Kullanƒ±cƒ± bilgisi bulunamadƒ±. L√ºtfen tekrar giri≈ü yapƒ±n.');
        navigate('/');
        return;
      }

      try {
        setInitialLoading(true);
        setFetchError(null);
        console.log('üîç Fetching instructor profile for user:', user.id);

        // √ñnce users koleksiyonunda kullanƒ±cƒ±yƒ± kontrol et
        const userRef = doc(db, 'users', user.id);
        const userSnap = await getDoc(userRef);

        if (!userSnap.exists()) {
          console.error('‚ùå User document not found in users collection');
          setFetchError('Kullanƒ±cƒ± bulunamadƒ±. L√ºtfen tekrar giri≈ü yapƒ±n.');
          navigate('/');
          return;
        }

        const userData = userSnap.data();
        const instructorRef = doc(db, 'instructors', user.id);
        const instructorSnap = await getDoc(instructorRef);

        if (instructorSnap.exists()) {
          const instructorData = instructorSnap.data();
          console.log('‚úÖ Instructor profile found:', instructorData);
          console.log('‚úÖ User data found:', userData);

          // Her iki koleksiyondan gelen verileri birle≈ütir
          const formData = {
            // Instructor koleksiyonundan gelen veriler
            displayName: instructorData.displayName || userData.displayName || '',
            bio: instructorData.bio || '',
            specialties: instructorData.specialties || [],
            experience: instructorData.experience || '',
            phoneNumber: instructorData.phoneNumber || '',
            countryCode: instructorData.countryCode || '+90',
            location: instructorData.location || '',
            photoURL: instructorData.photoURL || userData.photoURL || '',
            
            // User koleksiyonundan gelen veriler
            gender: userData.gender || '',
            age: userData.age,
            level: userData.level || 'beginner',
            city: userData.city || '',
            height: userData.height,
            weight: userData.weight,
            danceStyles: userData.danceStyles || [],
            availableTimes: userData.availableTimes || [],
            
            // Diƒüer alanlar
            createdAt: instructorData.createdAt || '',
            updatedAt: instructorData.updatedAt || '',
            isActive: instructorData.isActive || true,
            role: userData.role || 'instructor',
            userId: user.id
          };

          console.log('üìù Setting form data:', formData);
          reset(formData);
          setSelectedSpecialties(formData.specialties);
          setProfilePhotoURL(formData.photoURL);
          toast.success('Profil bilgileri y√ºklendi');
        } else {
          console.log('‚ÑπÔ∏è No instructor profile found, creating new instructor profile');
          
          // Yeni eƒüitmen profili olu≈ütur
          const newInstructorData: Partial<InstructorProfileFormData> = {
            displayName: userData.displayName || '',
            bio: '',
            specialties: [],
            experience: '',
            phoneNumber: '',
            countryCode: '+90',
            location: '',
            photoURL: userData.photoURL || '',
            gender: userData.gender || '',
            age: userData.age || null,
            level: userData.level || 'beginner' as DanceLevel,
            city: userData.city || '',
            height: userData.height || null,
            weight: userData.weight || null,
            danceStyles: userData.danceStyles || [],
            availableTimes: userData.availableTimes || [],
            createdAt: new Date().toISOString(),
            updatedAt: new Date().toISOString(),
            isActive: true,
            role: 'instructor',
            userId: user.id
          };

          try {
            await setDoc(instructorRef, newInstructorData);
            console.log('‚úÖ New instructor profile created:', newInstructorData);
            
            reset(newInstructorData as InstructorProfileFormData);
            setSelectedSpecialties([]);
            setProfilePhotoURL(userData.photoURL || '');
            toast.success('Yeni eƒüitmen profili olu≈üturuldu');
            
            // User dok√ºmanƒ±nƒ± da g√ºncelle
            await updateDoc(userRef, {
              role: 'instructor',
              updatedAt: new Date().toISOString()
            });
          } catch (error) {
            console.error('‚ùå Error creating instructor profile:', error);
            toast.error('Eƒüitmen profili olu≈üturulurken bir hata olu≈ütu');
            throw error;
          }
        }
      } catch (error) {
        console.error('‚ùå Error fetching/creating profile:', error);
        setFetchError('Profil bilgileri y√ºklenirken bir hata olu≈ütu. L√ºtfen tekrar deneyin.');
        toast.error('Profil bilgileri y√ºklenirken bir hata olu≈ütu');
      } finally {
        setInitialLoading(false);
      }
    };

    fetchInstructorProfile();
  }, [user, reset, setValue, getValues, navigate]);

  const onSubmit = async (data: InstructorProfileFormData) => {
    if (!user?.id) {
      console.error('‚ùå Cannot submit: User ID not found');
      toast.error('Kullanƒ±cƒ± bilgisi bulunamadƒ±');
      return;
    }

    console.log('üì§ Form verileri:', data);
    console.log('üìç Se√ßilen dans stilleri:', data.danceStyles);

    setLoading(true);
    setSaveSuccess(false);

    try {
      const updateTimestamp = new Date().toISOString();

      // Instructor'a √∂zel alanlar
      const instructorUpdates = {
        displayName: data.displayName.trim(),
        bio: data.bio.trim(),
        specialties: data.danceStyles || [],
        experience: data.experience.trim(),
        phoneNumber: data.phoneNumber.replace(/\s/g, ''),
        countryCode: data.countryCode,
        location: data.location.trim(),
        updatedAt: updateTimestamp,
        isActive: true,
        role: 'instructor'
      };

      // Users koleksiyonu i√ßin alanlar - bo≈ü deƒüerleri kontrol et
      const userUpdates = {
        displayName: data.displayName.trim(),
        gender: data.gender || 'Belirtilmemi≈ü',
        age: typeof data.age === 'number' ? data.age : 0,
        level: data.level || 'beginner',
        city: data.city || 'Belirtilmemi≈ü',
        height: typeof data.height === 'number' ? data.height : 0,
        weight: typeof data.weight === 'number' ? data.weight : 0,
        danceStyles: data.danceStyles || [],
        availableTimes: data.availableTimes || [],
        role: 'instructor',
        updatedAt: updateTimestamp
      };

      console.log('üìù G√ºncellenecek instructor verileri:', instructorUpdates);
      console.log('üìù G√ºncellenecek user verileri:', userUpdates);

      // Firestore referanslarƒ±
      const instructorRef = doc(db, 'instructors', user.id);
      const userRef = doc(db, 'users', user.id);

      // Batch write kullanarak her iki tabloyu da g√ºncelle
      const batch = writeBatch(db);
      batch.update(instructorRef, instructorUpdates);
      batch.update(userRef, userUpdates);

      // Batch'i commit et
      await batch.commit();
      console.log('‚úÖ T√ºm g√ºncellemeler tamamlandƒ±');

      setSaveSuccess(true);
      toast.success('Profil ba≈üarƒ±yla g√ºncellendi');

      // Form verilerini yeniden y√ºkle
      const [instructorSnap, userSnap] = await Promise.all([
        getDoc(instructorRef),
        getDoc(userRef)
      ]);

      if (instructorSnap.exists() && userSnap.exists()) {
        const instructorData = instructorSnap.data();
        const userData = userSnap.data();
        const updatedData = {
          ...instructorData,
          ...userData
        };
        console.log('üì• G√ºncel veriler:', updatedData);
        reset({
          ...data,
          ...updatedData
        });
      }

      setTimeout(() => {
        setSaveSuccess(false);
      }, 2000);

    } catch (error: any) {
      console.error('‚ùå G√ºncelleme hatasƒ±:', error);
      console.error('Hata detaylarƒ±:', {
        code: error.code,
        message: error.message,
        stack: error.stack
      });

      if (error.code === 'permission-denied') {
        toast.error('Bu i≈ülem i√ßin yetkiniz bulunmuyor');
      } else if (error.code === 'invalid-argument') {
        toast.error('Ge√ßersiz veri formatƒ±');
      } else if (error.code === 'not-found') {
        toast.error('Profil bulunamadƒ±');
      } else {
        toast.error('Profil g√ºncellenirken bir hata olu≈ütu');
      }
    } finally {
      setLoading(false);
    }
  };

  const handleImageUploadComplete = async (base64Image: string | null) => {
    if (!user?.id) {
      console.error('‚ùå Cannot update photo: User ID not found');
      toast.error('Kullanƒ±cƒ± bilgisi bulunamadƒ±');
      return;
    }

    if (!base64Image) {
      console.error('‚ùå No image data provided');
      toast.error('Fotoƒüraf verisi bulunamadƒ±');
      return;
    }

    console.log('üñºÔ∏è Starting profile photo update:', { userId: user.id, hasImage: !!base64Image });
    
    try {
      // √ñnce UI state'ini g√ºncelle
      setProfilePhotoURL(base64Image);
      setValue('photoURL', base64Image);

      // Batch write olu≈ütur
      const batch = writeBatch(db);
      const instructorRef = doc(db, 'instructors', user.id);
      const userRef = doc(db, 'users', user.id);

      const updateTimestamp = new Date().toISOString();
      const sharedUpdates = {
        photoURL: base64Image,
        updatedAt: updateTimestamp
      };

      // Batch'e g√ºncellemeleri ekle
      batch.update(instructorRef, sharedUpdates);
      batch.update(userRef, sharedUpdates);

      // Batch'i commit et
      await batch.commit();
      console.log('‚úÖ Batch write completed successfully');

      // ImageUploader'ƒ± sƒ±fƒ±rla
      setResetImageUploader(true);
      setTimeout(() => setResetImageUploader(false), 100);

      toast.success('Profil fotoƒürafƒ± g√ºncellendi');
    } catch (error: any) {
      // Hata durumunda UI'ƒ± eski haline getir
      setProfilePhotoURL(user.photoURL || '');
      setValue('photoURL', user.photoURL || '');
      console.error('‚ùå Error updating profile photo:', error);

      if (error.code === 'resource-exhausted') {
        toast.error('√áok fazla istek g√∂nderildi. L√ºtfen birka√ß saniye bekleyip tekrar deneyin.');
        // Otomatik yeniden deneme i√ßin timeout ekle
        setTimeout(() => {
          toast.success('≈ûimdi tekrar deneyebilirsiniz');
        }, 5000);
      } else {
        toast.error('Profil fotoƒürafƒ± g√ºncellenirken bir hata olu≈ütu');
      }
    }
  };

  // Dans stilleri deƒüi≈üikliƒüini handle eden fonksiyon
  const handleDanceStylesChange = (value: string | string[]) => {
    console.log('üé≠ Dans stilleri deƒüi≈üikliƒüi - gelen deƒüer:', value);
    const styles = Array.isArray(value) ? value : [value];
    const filteredStyles = value === '' ? [] : styles.filter(style => style !== '');
    console.log('üé≠ Filtrelenmi≈ü dans stilleri:', filteredStyles);
    setValue('danceStyles', filteredStyles);
    setSelectedSpecialties(filteredStyles);
  };

  // Dans stilleri se√ßimi i√ßin loading durumu
  const renderDanceStylesSelect = () => {
    if (loadingStyles) {
      return (
        <div className="flex items-center space-x-2 text-gray-500">
          <svg className="animate-spin h-5 w-5" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24">
            <circle className="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" strokeWidth="4"></circle>
            <path className="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"></path>
          </svg>
          <span>Dans stilleri y√ºkleniyor...</span>
        </div>
      );
    }

    return (
      <CustomSelect
        name="danceStyles"
        label="Dans Stilleri ve Uzmanlƒ±k Alanlarƒ±"
        value={watch('danceStyles')}
        onChange={handleDanceStylesChange}
        options={danceStyles}
        multiple={true}
        required
      />
    );
  };

  const renderTabs = () => (
    <div className="mb-8 border-b border-gray-200">
      <nav className="-mb-px flex space-x-8" aria-label="Tabs">
        <button
          onClick={() => setActiveTab('user')}
          className={`${
            activeTab === 'user'
              ? 'border-indigo-500 text-indigo-600'
              : 'border-transparent text-gray-500 hover:text-gray-700 hover:border-gray-300'
          } whitespace-nowrap py-4 px-1 border-b-2 font-medium text-sm`}
        >
          Kullanƒ±cƒ± Bilgileri
        </button>
        <button
          onClick={() => setActiveTab('instructor')}
          className={`${
            activeTab === 'instructor'
              ? 'border-indigo-500 text-indigo-600'
              : 'border-transparent text-gray-500 hover:text-gray-700 hover:border-gray-300'
          } whitespace-nowrap py-4 px-1 border-b-2 font-medium text-sm`}
        >
          Eƒüitmen Bilgileri
        </button>
      </nav>
    </div>
  );

  const renderUserInfoForm = () => (
    <div className="space-y-6">
      <div>
        <h3 className="text-lg font-medium text-gray-900 mb-4">Temel Bilgiler</h3>
        <p className="text-sm text-gray-500 mb-6">
          Bu bilgiler profilinizde g√∂r√ºnt√ºlenecek ve √∂ƒürencilerinizle e≈üle≈ümeniz i√ßin kullanƒ±lacaktƒ±r.
        </p>
      </div>

      <ImageUploader
        currentPhotoURL={profilePhotoURL}
        onImageChange={handleImageUploadComplete}
        displayName={watch('displayName') || ''}
        userType="instructor"
        shape="circle"
        width={150}
        height={150}
      />

      <div>
        <CustomInput
          name="displayName"
          label="Adƒ±nƒ±z Soyadƒ±nƒ±z"
          value={watch('displayName')}
          onChange={(e) => setValue('displayName', e.target.value)}
          required
        />
      </div>

      <div>
        <CustomSelect
          name="gender"
          label="Cinsiyet"
          value={watch('gender')}
          onChange={(value: string | string[]) => setValue('gender', value as string)}
          options={[
            { value: 'male', label: 'Erkek' },
            { value: 'female', label: 'Kadƒ±n' },
            { value: 'other', label: 'Diƒüer' }
          ]}
          required
        />
      </div>

      <div>
        <CustomInput
          type="text"
          name="age"
          label="Ya≈ü"
          value={watch('age')?.toString() || ''}
          onChange={(e) => {
            const value = e.target.value ? parseInt(e.target.value) : undefined;
            setValue('age', value);
          }}
          required
        />
      </div>

      <div>
        <CustomSelect
          name="level"
          label="Dans Seviyesi"
          value={watch('level')}
          onChange={(value: string | string[]) => setValue('level', value as DanceLevel)}
          options={[
            { value: 'beginner', label: 'Ba≈ülangƒ±√ß' },
            { value: 'intermediate', label: 'Orta Seviye' },
            { value: 'advanced', label: 'ƒ∞leri Seviye' },
            { value: 'professional', label: 'Profesyonel' }
          ]}
          required
        />
      </div>

      <div>
        <CustomInput
          name="city"
          label="≈ûehir"
          value={watch('city')}
          onChange={(e) => setValue('city', e.target.value)}
          required
          placeholder="√ñrn: ƒ∞stanbul, Ankara"
        />
      </div>

      <div>
        <CustomInput
          type="text"
          name="height"
          label="Boy (cm)"
          value={watch('height')?.toString() || ''}
          onChange={(e) => {
            const value = e.target.value ? parseInt(e.target.value) : undefined;
            setValue('height', value);
          }}
          placeholder="√ñrn: 175"
        />
      </div>

      <div>
        <CustomInput
          type="text"
          name="weight"
          label="Kilo (kg)"
          value={watch('weight')?.toString() || ''}
          onChange={(e) => {
            const value = e.target.value ? parseInt(e.target.value) : undefined;
            setValue('weight', value);
          }}
          placeholder="√ñrn: 70"
        />
      </div>
    </div>
  );

  const renderInstructorInfoForm = () => (
    <div className="space-y-6">
      <div>
        <h3 className="text-lg font-medium text-gray-900 mb-4">Eƒüitmen Bilgileri</h3>
        <p className="text-sm text-gray-500 mb-6">
          Bu bilgiler eƒüitmenlik profilinizde g√∂r√ºnt√ºlenecek ve √∂ƒürencilerinizle e≈üle≈ümeniz i√ßin kullanƒ±lacaktƒ±r.
        </p>
      </div>

      <div>
        {renderDanceStylesSelect()}
        <p className="mt-2 text-sm text-gray-500">
          Se√ßtiƒüiniz dans stilleri hem uzmanlƒ±k alanlarƒ±nƒ±z hem de dans stilleriniz olarak kaydedilecektir.
        </p>
      </div>

      <div>
        <CustomInput
          name="bio"
          label="Biyografi"
          value={watch('bio')}
          onChange={(e) => setValue('bio', e.target.value)}
          multiline
          rows={4}
          placeholder="Kendinizi ve dans deneyiminizi anlatƒ±n..."
        />
      </div>

      <div>
        <CustomInput
          name="experience"
          label="Deneyim"
          value={watch('experience')}
          onChange={(e) => setValue('experience', e.target.value)}
          placeholder="√ñrn: 5 yƒ±l profesyonel dans eƒüitmenliƒüi"
        />
      </div>

      <div>
        <CustomPhoneInput
          name="phoneNumber"
          label="Telefon Numarasƒ±"
          countryCode={watch('countryCode')}
          phoneNumber={watch('phoneNumber')}
          onCountryCodeChange={(value) => setValue('countryCode', value)}
          onPhoneNumberChange={(value) => setValue('phoneNumber', value)}
        />
      </div>

      <div>
        <CustomInput
          name="location"
          label="Ders Verdiƒüiniz Lokasyon"
          value={watch('location')}
          onChange={(e) => setValue('location', e.target.value)}
          placeholder="√ñrn: Kadƒ±k√∂y, ƒ∞stanbul"
        />
      </div>
    </div>
  );

  if (initialLoading) {
    return (
      <div className="flex flex-col items-center justify-center min-h-[400px] bg-white rounded-lg shadow-sm p-6">
        <div className="animate-spin rounded-full h-12 w-12 border-t-2 border-b-2 border-indigo-500"></div>
        <p className="mt-4 text-gray-600">Profil bilgileri y√ºkleniyor...</p>
      </div>
    );
  }

  if (fetchError) {
    return (
      <div className="bg-white rounded-lg shadow-sm p-6">
        <div className="text-center">
          <div className="text-red-500 mb-4">
            <svg className="h-12 w-12 mx-auto" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M12 8v4m0 4h.01M21 12a9 9 0 11-18 0 9 9 0 0118 0z" />
            </svg>
          </div>
          <h3 className="text-lg font-medium text-gray-900 mb-2">Hata Olu≈ütu</h3>
          <p className="text-gray-600 mb-4">{fetchError}</p>
          <button
            onClick={() => window.location.reload()}
            className="inline-flex items-center px-4 py-2 border border-transparent text-sm font-medium rounded-md text-white bg-indigo-600 hover:bg-indigo-700 focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-indigo-500"
          >
            Yeniden Dene
          </button>
        </div>
      </div>
    );
  }

  return (
    <div className="max-w-3xl mx-auto py-8 px-4 sm:px-6 lg:px-8">
      {renderTabs()}
      
      <form onSubmit={handleSubmit(onSubmit)} className="space-y-8">
        {activeTab === 'user' ? renderUserInfoForm() : renderInstructorInfoForm()}
        
        <div className="flex justify-between pt-6 border-t border-gray-200">
          <button
            type="button"
            onClick={() => setActiveTab(activeTab === 'user' ? 'instructor' : 'user')}
            className="inline-flex items-center px-4 py-2 border border-gray-300 shadow-sm text-sm font-medium rounded-md text-gray-700 bg-white hover:bg-gray-50 focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-indigo-500"
          >
            {activeTab === 'user' ? 'Eƒüitmen Bilgilerine Ge√ß' : 'Kullanƒ±cƒ± Bilgilerine D√∂n'}
          </button>
          
          <button
            type="submit"
            disabled={loading}
            className="inline-flex items-center px-4 py-2 border border-transparent text-sm font-medium rounded-md shadow-sm text-white bg-indigo-600 hover:bg-indigo-700 focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-indigo-500 disabled:opacity-50"
          >
            {loading ? (
              <>
                <svg className="animate-spin -ml-1 mr-2 h-4 w-4 text-white" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24">
                  <circle className="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" strokeWidth="4"></circle>
                  <path className="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"></path>
                </svg>
                Kaydediliyor...
              </>
            ) : (
              'Kaydet'
            )}
          </button>
        </div>
      </form>

      {saveSuccess && (
        <div className="mt-4 p-4 bg-green-50 rounded-md">
          <div className="flex">
            <div className="flex-shrink-0">
              <svg className="h-5 w-5 text-green-400" xmlns="http://www.w3.org/2000/svg" viewBox="0 0 20 20" fill="currentColor">
                <path fillRule="evenodd" d="M10 18a8 8 0 100-16 8 8 0 000 16zm3.707-9.293a1 1 0 00-1.414-1.414L9 10.586 7.707 9.293a1 1 0 00-1.414 1.414l2 2a1 1 0 001.414 0l4-4z" clipRule="evenodd" />
              </svg>
            </div>
            <div className="ml-3">
              <p className="text-sm font-medium text-green-800">
                Profiliniz ba≈üarƒ±yla g√ºncellendi!
              </p>
            </div>
          </div>
        </div>
      )}
    </div>
  );
};

